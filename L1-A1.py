num1 = 10
num2 = 4

print("Num1 and Num2 are: ", num1, num2)
print("Num1 | Num2 is: ", num1 | num2) #OR operator
print("Num1 & Num2 is: ", num1 & num2) #AND operator
print("Num1 ^ Num2 is: ", num1 ^ num2) #XOR operator
print("Num1 << Num2 is: ", num1 << num2) #Left shift operator
print("Num1 >> Num2 is: ", num1 >> num2) #Right shift operator
print("~Num1 is: ", ~num1) #Negation operator
print("~Num2 is: ", ~num2)